#include "libft.h"

char	*ft_strstr(const char *hs, const char *ne)
{
	size_t i;
	size_t j;

	i = 0;
	while (hs[i] != '\0')
	{
		j = 0;
		while (ne[j] == hs[i + j])
		{
			if (ne[j + i] == '\0')
			{
				return (hs + i);
			}
			j++;
		}
		i++;
	}
	return (0);
}
