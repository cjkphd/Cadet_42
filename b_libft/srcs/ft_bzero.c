#include "../includes/libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*temp;
	size_t	i;

	if (n == 0)
		return ;
	temp = s;
	i = 0;
	while (i < n)
	{
		temp[i] = 0;
		i++;
	}
}
